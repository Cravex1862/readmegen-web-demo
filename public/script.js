// Detect environment
(async function detectEnv() {
  try {
    const res = await fetch("/api/ping", { method: "HEAD", signal: AbortSignal.timeout(2000) });
    if (res.ok) {
      badgeEl.textContent = "Server mode (full readmegen)";
      badgeEl.className = "badge backend";
    } else {
      throw new Error("no backend");
    }
  } catch {
    badgeEl.textContent = "Pages mode (GitHub API)";
    badgeEl.className = "badge pages";
  }
})();

generateBtn.addEventListener("click", async () => {
  const url = repoUrl.value.trim();
  if (!url) {
    setStatus("error", "Enter a repository URL");
    return;
  }

  hideStatus();
  generateBtn.disabled = true;
  generateBtn.textContent = "Generating...";
  resultEl.classList.add("hidden");

  try {
    let result;
    try {
      result = await generateViaBackend(url);
      badgeEl.textContent = "Backend: " + result.source;
      badgeEl.className = "badge backend";
    } catch {
      result = await generateViaGitHubApi(url);
      badgeEl.textContent = "Pages mode: " + result.source;
      badgeEl.className = "badge pages";
    }

    lastContent = result.content;
    previewEl.textContent = result.content;
    lineCountEl.textContent = `${result.content.split("\n").length} lines`;
    resultEl.classList.remove("hidden");
    setStatus("success", "README generated");
  } catch (err) {
    setStatus("error", err.message);
  } finally {
    generateBtn.disabled = false;
    generateBtn.textContent = "Generate";
  }
});

repoUrl.addEventListener("keydown", (e) => {
  if (e.key === "Enter") generateBtn.click();
});

copyBtn.addEventListener("click", async () => {
  try {
    await navigator.clipboard.writeText(lastContent);
    const orig = copyBtn.textContent;
    copyBtn.textContent = "Copied!";
    setTimeout(() => copyBtn.textContent = orig, 1500);
  } catch {
    copyBtn.textContent = "Failed";
  }
});

downloadBtn.addEventListener("click", () => {
  const blob = new Blob([lastContent], { type: "text/markdown" });
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = "README.md";
  a.click();
  URL.revokeObjectURL(a.href);
});
