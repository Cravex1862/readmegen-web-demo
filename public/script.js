let lastContent = "";

function setStatus(type, msg) {
  statusEl.className = type;
  statusEl.textContent = msg;
  statusEl.classList.remove("hidden");
}

function hideStatus() {
  statusEl.classList.add("hidden");
}

function parseRepo(url) {
  url = url.trim().replace(/\.git$/, "");
  const m = url.match(/github\.com[:\/]([^\/]+)\/([^\/]+)/);
  if (!m) return null;
  return { owner: m[1], repo: m[2].replace(/\/$/, ""), full: `${m[1]}/${m[2].replace(/\/$/, "")}` };
}

// --- Backend mode ---

async function generateViaBackend(url) {
  const res = await fetch("/api/generate", {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify({ url }),
  });
  const data = await res.json();
  if (!res.ok) throw new Error(data.error);
  return { content: data.content, source: "readmegen (AI templates)" };
}

// --- GitHub API fallback ---

async function fetchJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`GitHub API error: ${res.status}`);
  return res.json();
}

async function generateViaGitHubApi(url) {
  const repo = parseRepo(url);
  if (!repo) throw new Error("Could not parse GitHub repo URL");

  const repoInfo = await fetchJson(`https://api.github.com/repos/${repo.full}`);
  const branch = repoInfo.default_branch;

  const tree = await fetchJson(
    `https://api.github.com/repos/${repo.full}/git/trees/${branch}?recursive=1`,
  );

  const files = tree.tree.map(t => t.path);
  const dirs = new Set();
  const fileList = [];
  for (const f of files) {
    const parts = f.split("/");
    for (let i = 0; i < parts.length - 1; i++) {
      dirs.add(parts.slice(0, i + 1).join("/"));
    }
    if (!f.endsWith("/")) fileList.push(f);
  }

  const name = repo.repo;
  const description = repoInfo.description || "No description provided";
  const language = repoInfo.language || "Unknown";

  const packageJson = await tryFetchRaw(repo.full, branch, "package.json");
  const requirementsRaw = await tryFetchRaw(repo.full, branch, "requirements.txt");
  const cargoTomlRaw = await tryFetchRaw(repo.full, branch, "Cargo.toml");

  let deps = {};
  let scripts = {};
  if (packageJson) {
    try {
      const p = JSON.parse(atob(packageJson));
      deps = { ...p.dependencies, ...p.devDependencies };
      scripts = p.scripts || {};
    } catch {}
  }

  const depLines = Object.entries(deps).length
    ? Object.entries(deps).slice(0, 20).map(([k, v]) => `| \`${k}\` | ${v} |`).join("\n")
    : "None";

  const scriptLines = Object.entries(scripts).length
    ? Object.entries(scripts).slice(0, 10).map(([k, v]) => `| \`${k}\` | \`${v}\` |`).join("\n")
    : "None";

  const treeLines = buildTreeLines(tree.tree, repo.repo);
  const treeStr = "```\n" + treeLines.join("\n") + "\n```";

  const readme = `# ${name}

${description}

## Table of Contents
- [Installation](#installation)
- [Usage](#usage)
- [Scripts](#scripts)
- [Dependencies](#dependencies)
- [File Tree](#file-tree)
- [Contributing](#contributing)
- [License](#license)

## Installation

\`\`\`bash
git clone https://github.com/${repo.full}.git
cd ${name}
\`\`\`

## Usage

_Generated via GitHub API — run the full readmegen tool locally for AI-powered descriptions._

\`\`\`bash
readmegen ${name}
\`\`\`

## Scripts

| Script | Command |
|--------|---------|
${scriptLines}

## Dependencies

| Package | Version |
|---------|---------|
${depLines}

## File Tree

${treeStr}

## Contributing

Contributions are welcome! Please open an issue or submit a pull request.

## License

${repoInfo.license?.spdx_id || "MIT"}

---

_Generated by [readmegen](https://github.com/Cravex1862/readmegen) via the GitHub API (no AI templates available in browser mode)_
`;

  return { content: readme, source: "GitHub API (static template)" };
}

async function tryFetchRaw(full, branch, filePath) {
  try {
    const res = await fetch(`https://raw.githubusercontent.com/${full}/${branch}/${filePath}`);
    if (!res.ok) return null;
    return await res.text();
  } catch {
    return null;
  }
}

function buildTreeLines(treeItems, root) {
  const result = [`${root}/`];
  const sorted = treeItems.filter(t => t.type === "blob").map(t => t.path).sort();
  const dirs = new Set();
  for (const f of sorted) {
    const parts = f.split("/");
    for (let i = 0; i < parts.length; i++) {
      const parent = parts.slice(0, i).join("/");
      if (i > 0) dirs.add(parent);
    }
  }

  const all = [...dirs].sort();
  for (const d of all) {
    const depth = d.split("/").length;
    const prefix = "  ".repeat(depth) + "├── ";
    const label = d.split("/").pop();
    if (!sorted.some(f => f.startsWith(d + "/") || f === d)) continue;
    result.push(prefix + label + "/");
  }

  for (const f of sorted) {
    const depth = f.split("/").length;
    const prefix = "  ".repeat(depth) + "├── ";
    result.push(prefix + f.split("/").pop());
  }

  return result.slice(0, 50);
}

